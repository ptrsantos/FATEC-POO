    private void jTableClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableClienteMouseClicked
        int indice = this.jTableCliente.getSelectedRow();
        //System.out.println("Ã¬ndice da lista = " + indice);
        String dados[] = new String[5];
        ControleCliente cl = new ControleCliente();
        if(indice >= 0 && indice < cl.listarCliente().size()){
            dados[0] = String.valueOf(cl.listarCliente().get(indice).getIdCliente());
            dados[1] = cl.listarCliente().get(indice).getNomeDoCliente();
            dados[2] = cl.listarCliente().get(indice).getRg();
            dados[3] = cl.listarCliente().get(indice).getEmail();
            dados[4] = cl.listarCliente().get(indice).getTelefone();
        }
        fc.setFlag(2);
        fc.controlaEstadoBotoes();
        fc.recebeLista(dados);
        fc.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_jTableClienteMouseClicked

